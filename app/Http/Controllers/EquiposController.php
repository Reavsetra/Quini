     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $equipo = Equipos::find($id);
        $equipo->fill($request->all());
        $equipo->save();

        return redirect()->route('equipos.create');
    }

